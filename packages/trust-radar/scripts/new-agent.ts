#!/usr/bin/env tsx
/**
 * `pnpm new-agent` scaffolder — MVP (Phase 5.3a).
 *
 * Emits a compliant AgentModule skeleton. Eliminates the wiring-drift
 * problem at the source by giving authors a starting point that
 * already has every required field declared (Phase 4's interface
 * tightening means a missing field fails typecheck).
 *
 * Phase 5.3a scope: write the module file ONLY.
 * Phase 5.3b adds: AgentName union + agents/index.ts registry patches
 * Phase 5.3c adds: averrow-ui metadata + AGENT_GROUPS + AgentIcon
 *                  placeholder + docs/AI_AGENTS.md row patches.
 *
 * Author flow today (5.3a):
 *   1. pnpm new-agent <snake_name> --display "..." [flags below]
 *   2. Open the emitted file. Replace placeholder TODO blocks with
 *      the agent's actual logic.
 *   3. Run pnpm audit:agent-standard — it lists the remaining wiring
 *      points to add by hand (registry, UI metadata, icon, doc).
 *   4. Open PR. CI runs the audit + drift gates so nothing ships
 *      half-wired.
 *
 * Usage:
 *   pnpm new-agent recon-deux \
 *     --display "Recon Deux" \
 *     --category intelligence \
 *     --trigger scheduled
 *
 * Required flags:
 *   --display    Human-readable display name (e.g. "Recon Deux")
 *
 * Optional flags:
 *   --category   AgentCategory: orchestration | intelligence |
 *                response | ops | meta | sync (default: intelligence)
 *   --trigger    TriggerType: scheduled | event | api | manual
 *                (default: scheduled)
 *   --color      Hex color for the agent (default: #38BDF8)
 */

import { writeFileSync, existsSync, readFileSync } from "node:fs";
import { resolve } from "node:path";

// ─── Args ────────────────────────────────────────────────────────

interface ScaffoldArgs {
  snakeName: string;
  displayName: string;
  category: string;
  trigger: string;
  color: string;
}

const VALID_CATEGORIES = ["orchestration", "intelligence", "response", "ops", "meta", "sync"];
const VALID_TRIGGERS = ["scheduled", "event", "api", "manual"];

function parseArgs(argv: string[]): ScaffoldArgs {
  const positional: string[] = [];
  const flags = new Map<string, string>();
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i]!;
    if (arg.startsWith("--")) {
      const key = arg.slice(2);
      const next = argv[i + 1];
      if (next === undefined || next.startsWith("--")) {
        die(`Flag --${key} expects a value`);
      }
      flags.set(key, next);
      i++;
    } else {
      positional.push(arg);
    }
  }

  const snakeName = positional[0];
  if (!snakeName) die("Missing positional arg: <snake_name>");
  if (!/^[a-z][a-z0-9_]*$/.test(snakeName)) {
    die(`agent name must be snake_case (lowercase letters, digits, underscores) — got "${snakeName}"`);
  }

  const displayName = flags.get("display");
  if (!displayName) die("Missing required flag: --display \"Display Name\"");

  const category = flags.get("category") ?? "intelligence";
  if (!VALID_CATEGORIES.includes(category)) {
    die(`--category must be one of ${VALID_CATEGORIES.join("|")} — got "${category}"`);
  }

  const trigger = flags.get("trigger") ?? "scheduled";
  if (!VALID_TRIGGERS.includes(trigger)) {
    die(`--trigger must be one of ${VALID_TRIGGERS.join("|")} — got "${trigger}"`);
  }

  const color = flags.get("color") ?? "#38BDF8";
  if (!/^#[0-9A-Fa-f]{6}$/.test(color)) {
    die(`--color must be a 6-digit hex value (e.g. #38BDF8) — got "${color}"`);
  }

  return { snakeName, displayName, category, trigger, color };
}

function die(msg: string): never {
  console.error(`[new-agent] ${msg}`);
  console.error("Usage: pnpm new-agent <snake_name> --display \"Display Name\" [--category ...] [--trigger ...] [--color #hex]");
  process.exit(1);
}

// ─── Naming helpers ──────────────────────────────────────────────

function snakeToCamel(snake: string): string {
  return snake.replace(/_([a-z0-9])/g, (_m, c: string) => c.toUpperCase());
}
function snakeToKebab(snake: string): string { return snake.replace(/_/g, "-"); }
function snakeToPascal(snake: string): string {
  const camel = snakeToCamel(snake);
  return camel.charAt(0).toUpperCase() + camel.slice(1);
}

